
a = float(input("Въведи страна a: "))
b = float(input("Въведи страна b: "))
c = float(input("Въведи страна c: "))


if a > 0 and b > 0 and c > 0:
    
    if a + b > c and a + c > b and b + c > a:
        print("Тези страни могат да образуват триъгълник.")
    else:
        print("Тези страни НЕ могат да образуват триъгълник.")
else:
    print("Всички страни трябва да са положителни числа.")